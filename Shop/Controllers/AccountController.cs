        /// <summary>
        /// Sprawdza czy email danego użytkownika został potwierdzony.
        /// </summary>
        /// <param name="userId"></param>
        /// <param name="code"></param>
        /// <returns>Widok z potwierdzeniem lub errorem.</returns>
